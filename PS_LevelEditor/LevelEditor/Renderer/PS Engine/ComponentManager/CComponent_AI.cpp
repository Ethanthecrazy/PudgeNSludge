/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// CComponent_AI():	Default Constructor
//
// Returns:		Void
//
// Mod. Name: JF
// Mod. Date: 6/2/12
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
CComponent_AI::CComponent_AI () : IComponent (ECOMP_AI)
{
	m_aiType = AI_NULL;
	m_pTarget = NULL;
	m_pCurPlatform = NULL;
	m_bOnGround = false;
	m_v3Velocity = vec3f (0.0f, 0.0f, 0.0f);
	m_bOnGround = false;
	m_bActive = false;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// CComponent_AI():	Default Destructor
//
// Returns:		Void
//
// Mod. Name: JF
// Mod. Date: 6/2/12
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
CComponent_AI::~CComponent_AI ()
{
	Shutdown ();
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Initialize():	virtual Initialize, Inits all ai information
//
// Returns:		bool
//
// Mod. Name: JF
// Mod. Date: 6/2/12
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
bool CComponent_AI::Initialize ()
{
	// Init default values to null
	m_aiType = AI_NULL;
	m_pTarget = NULL;
	m_pCurPlatform = NULL;
	m_v3Velocity = vec3f (0.0f, 0.0f, 0.0f);
	m_bOnGround = false;
	m_bActive = true;
	return true;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Shutdown():	virtual Shutdown
//
// Returns:		Void
//
// Mod. Name: JF
// Mod. Date: 6/2/12
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void CComponent_AI::Shutdown ()
{
	// set the target and ai type to null
	m_pTarget = NULL;
	m_aiType = AI_NULL;
	m_pTarget = NULL;
	m_pCurPlatform = NULL;
	m_v3Velocity = vec3f (0.0f, 0.0f, 0.0f);
	m_bOnGround = false;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	// Update(float _fElapsedTime):	virtual Update, updates the ai information
	//
	// Returns:		Void
	//
	// Mod. Name: JF
	// Mod. Date: 6/2/12
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void CComponent_AI::Update ( float _fElapsedTime )
{
	// there is no AI so dont update
	if (m_aiType == AI_NULL)
	{
		return;
	}
}